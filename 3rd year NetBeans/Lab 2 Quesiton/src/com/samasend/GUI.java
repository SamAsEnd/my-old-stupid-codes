    private void passActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_passActionPerformed
        String name = (String) jComboBox1.getSelectedItem();

        int i1 = Integer.parseInt(rf.getText());
        int i2 = Integer.parseInt(pg.getText());
        int i3 = Integer.parseInt(ap.getText());
        int i4 = Integer.parseInt(gdp.getText());
        int i5 = Integer.parseInt(gr.getText());

        double avg = (i1 + i2 + i3 + i4 + i5) / 5.0;

        String status = "";

        if (avg > 90) {
            status = "Excellent";
        } else if (avg > 80) {
            status = "Very Good";
        } else if (avg > 70) {
            status = "Good";
        } else if (avg > 60) {
            status = "Fair";
        } else if (avg > 50) {
            status = "Poor";
        } else {
            status = "Bad";
        }
        Country country = new Country(name, avg, status);
        countries.add(country);
        Collections.sort(countries, new Comparator<Country>() {
            @Override
            public int compare(Country o1, Country o2) {
                return Double.compare(o2.getAvg(),o1.getAvg());
            }
        });
        updateTable();
    }//GEN-LAST:event_passActionPerformed

    private void updateTable() {

        Stack<Stack<String>> data = new Stack<>();

        for (Country country : countries) {
            Stack<String> row = new Stack<>();

            row.add(country.getName());
            row.add("" + country.getAvg());
            row.add(country.getStatus());

            data.add(row);
        }

        Stack<String> title = new Stack<>();

        title.push("Name");
        title.push("AVG");
        title.push("Status");

        table.setModel(new DefaultTableModel(data, title));

        table.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                updateImg();
            }
        });
    }

    private void updateImg() {
        int selectedIndex = table.getSelectedRow();
        if (selectedIndex == -1) {
            displayFlag.setIcon(null);
        } else {
            Country country = countries.get(selectedIndex);
            ImageIcon icon = new ImageIcon(getClass().getResource("/com/samasend/flag/" + country.getName() + ".png"));
            displayFlag.setIcon(icon);
        }
    }

