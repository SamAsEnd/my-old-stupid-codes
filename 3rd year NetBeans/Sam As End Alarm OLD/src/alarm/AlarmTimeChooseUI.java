    private ComboBoxModel getDateModel() {
        final int[] lastDate = {31,29,31,30,31,30,31,31,30,31,30,31};
        int m = month.getSelectedIndex();
        DefaultComboBoxModel<String> r = new DefaultComboBoxModel<>();
        for(int i=1;i<=lastDate[m];i++) {
            r.addElement(""+i);
        }
        return r;
    }
    private final static int HOUR = 0;
    private final static int MIN = 1;
    private final static int SEC = 2;
    
    private ComboBoxModel getModel(int m) {
        final int[] lastDate = {23,59,59};
        DefaultComboBoxModel<String> r = new DefaultComboBoxModel<>();
        for(int i=0;i<=lastDate[m];i++) {
            r.addElement(""+i);
        }
        return r;
    }

