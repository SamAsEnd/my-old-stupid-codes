    private void saveToFile(JTextArea component) throws HeadlessException {
        try {
            JFileChooser chooser = new JFileChooser();
            do {
                int returnVal = chooser.showSaveDialog(this);
                if (returnVal == JFileChooser.APPROVE_OPTION) {
                    File file = chooser.getSelectedFile();
                    if (file.exists()) {
                        int confirm = JOptionPane.showConfirmDialog(this, "File already exist. Replace? ");
                        if (confirm != JOptionPane.YES_OPTION) {
                            continue;
                        }
                    }
                    if (!file.getName().toUpperCase().endsWith(".TXT")) {
                        file = new File(file.getCanonicalPath() + ".txt");
                    }
                    String txt = component.getText();
                    PrintStream ps = new PrintStream(file);
                    ps.print(txt);
                }
                break;
            } while (true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "File Not Found!!!");
        }
    }

