    private void pf_recalculateKey() {
        playFairCipher.setKey(pf_key.getText());
        Stack<Stack<Character>> matrix = playFairCipher.getMatrix();
        Stack<Character> stack = new Stack<Character>();

        stack.push(' ');
        stack.push(' ');
        stack.push(' ');
        stack.push(' ');
        stack.push(' ');

        pf_tableKey.setModel(new DefaultTableModel(matrix, stack));
    }

    private void hc_display(int[][] key) {
        Stack<Stack<Integer>> integers = new Stack<Stack<Integer>>();
        int size = 0;
        for (int i = 0; i < key.length; i++) {
            int[] is = key[i];
            Stack<Integer> stack = new Stack<Integer>();
            for (int j = 0; j < is.length; j++) {
                int k = is[j];
                stack.push(k);
            }
            size = is.length;
            integers.push(stack);
        }
        Stack<String> t = new Stack<String>();
        while (size > 0) {
            t.push("");
            size--;
        }
        hillDisplay.setModel(new DefaultTableModel(integers, t));
    }
