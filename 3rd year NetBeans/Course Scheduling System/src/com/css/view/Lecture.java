    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {

            ResultSet result = statement.executeQuery("SELECT * FROM deptteach,course"
                    + " where dtCcode = cCode"
                    + " AND cName LIKE '%" + courseSearchField.getText()
                    + "%' AND dtDeptID = " + deptId);
            Vector<String> courses = new Vector<>();
            while (result.next()) {
                courses.add(result.getString("cCode"));
            }

            result = statement.executeQuery("SELECT * FROM lectureteach"
                    + " where ltCcode = '" + courses.get(teachCourse.getSelectedIndex())
                    + "' AND ltLssn = " + ssn);

            if (result.next()) {
                JOptionPane.showMessageDialog(null, "You already teaching this course!!!", "",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }

            int row = statement.executeUpdate("insert into lectureteach values("
                    + ssn + ",'" + courses.get(teachCourse.getSelectedIndex()) + "')");
            if (row <= 0) {
                JOptionPane.showMessageDialog(null, "Error Happened", "",
                        JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Successfully added", "",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

