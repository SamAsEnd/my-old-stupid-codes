    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedIndex = dropCourse.getSelectedIndex();
        if (selectedIndex < 0) {
            JOptionPane.showMessageDialog(this, "Select the course first!!!", "",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            ResultSet result = statement.executeQuery("SELECT * FROM lectureteach,course"
                    + " where ltCcode = cCode"
                    + " AND cName LIKE '%" + courseSearchField.getText() + "%'");
            Vector<String> courses = new Vector<>();
            while (result.next()) {
                courses.add(result.getString("cCode"));
            }
            // delete
            statement.executeUpdate("DELETE from lectureteach"
                    + " where ltLssn = " + ssn
                    + " AND ltCcode = '" + courses.get(selectedIndex) + "'");
            dropTeachReload();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

