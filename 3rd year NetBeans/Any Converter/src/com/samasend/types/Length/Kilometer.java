package com.samasend.types.Length;

/**
 *
 * @author SamAsEnd
 */
public class Kilometer implements Length{

	private double value;

	public Kilometer(double value) throws Exception {
		this.value = value;
	}


	@Override
	public double toAngstrom() {
		return value;
	}


	@Override
	public double toCentimeter() {
		return value;
	}


	@Override
	public double toChain() {
		return value;
	}


	@Override
	public double toFathom() {
		return value;
	}


	@Override
	public double toFeet() {
		return value;
	}


	@Override
	public double toHand() {
		return value;
	}


	@Override
	public double toInch() {
		return value;
	}


	@Override
	public double toKilometer() {
		return value;
	}


	@Override
	public double toLink() {
		return value;
	}


	@Override
	public double toMeter() {
		return value;
	}


	@Override
	public double toMicron() {
		return value;
	}


	@Override
	public double toMile() {
		return value;
	}


	@Override
	public double toMilliMeter() {
		return value;
	}


	@Override
	public double toNanoMeter() {
		return value;
	}


	@Override
	public double toNauticalMiles() {
		return value;
	}


	@Override
	public double toPICA() {
		return value;
	}


	@Override
	public double toRods() {
		return value;
	}


	@Override
	public double toSpan() {
		return value;
	}


	@Override
	public double toYard() {
		return value;
	}

}
