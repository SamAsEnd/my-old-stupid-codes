    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String bool = "0";

        try {
            String fname = txtinsert1.getText();
            String lname = txtinsert2.getText();
            String sal = txtinsert3.getText();
            double salary = Double.parseDouble(sal);
            String date = (String) jComboBox2.getSelectedItem() + jComboBox3.getSelectedItem() + jComboBox4.getSelectedItem();
            String employid = txtinsert5.getText();
            String dept = txtinsert6.getText();
            //int dept=Integer.parseInt(dnum);
            String sex = (String) jComboBox1.getSelectedItem();

            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/employeeMGTSYS", "root", "");
            PreparedStatement ts = con.prepareStatement("INSERT INTO employee values(?,?,?,?,?,?,?)");
            ts.setString(1, fname);
            ts.setString(2, lname);
            ts.setDouble(3, salary);
            ts.setString(4, date);
            ts.setString(5, employid);
            ts.setString(6, dept);
            ts.setString(7, sex);
            ts.executeUpdate();
            bool = "1";
            con.close();
        } catch (ClassNotFoundException | SQLException e) {
            System.out.println(e.getMessage());
        }
        if (bool.equals("1")) {
            jLabel11.setText(" Student registered successfully");
        } else {
            jLabel11.setText(" Studet  is not registered ,please  try again");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

