    public static void getChoose(JTextArea t) {
        String font = t.getFont().getName();
        int size = t.getFont().getSize();
        Font f = t.getFont();
        int tt = 0;
        if (f.isItalic()) {
            tt = 1;
        } else if (f.isBold()) {
            tt = 2;
        } else {
            tt = 0;
        }
        new FontChooser(font, size, tt).setVisible(true);
        text = t;
    }
