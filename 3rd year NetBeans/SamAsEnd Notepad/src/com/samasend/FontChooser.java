    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        String f = (String) fontList.getSelectedItem();
        Integer s = (Integer) sizeList.getSelectedItem();
        if (s == null) {
            s = new Integer(t.getFont().getSize());
        }
        int tmp = type.getSelectedIndex();
        if (tmp == 1) {
            tmp = Font.ITALIC;
        } else if (tmp == 2) {
            tmp = Font.BOLD;
        } else {
            tmp = Font.PLAIN;
        }
        text.setFont(new Font(f, tmp, s.intValue()));
        String userHome = System.getProperty("user.home");
        File fontFile;
        fontFile = new File(userHome, "___.txt");

        //set hidden not implemented
        BufferedWriter bufferedWriter = null;
        try {
            bufferedWriter = new BufferedWriter(new FileWriter(fontFile));
        } catch (IOException ex) {
        }
        StringBuilder sb = new StringBuilder();
        sb.append(f + "\n");
        sb.append(tmp + "\n");
        sb.append(s.intValue() + "\n");
        try {
            bufferedWriter.write(sb.toString());
            bufferedWriter.close();
        } catch (IOException ex) {
        }
        this.dispose();
    }//GEN-LAST:event_okActionPerformed

