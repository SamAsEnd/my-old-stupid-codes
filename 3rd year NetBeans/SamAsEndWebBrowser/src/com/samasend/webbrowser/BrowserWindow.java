    private void goActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_goActionPerformed
        String address = addressBar.getText();
        try {
            url = new URL(address);
        } catch (MalformedURLException ex) {
            url = url = getClass().getResource("error.html");
            System.out.println(ex);
        }
        new Thread(new Runnable() {
            @Override
            public void run() {
                loadContent();
                try {
                    Thread.currentThread().join();
                } catch (InterruptedException ex) {}
            }
        }).start();
    }//GEN-LAST:event_goActionPerformed
    
    private void txtHyperlinkUpdate(javax.swing.event.HyperlinkEvent evt) {//GEN-FIRST:event_txtHyperlinkUpdate
        url = evt.getURL();
        String str = evt.toString();
        String[] ses = str.split(",");
        StringBuilder sb = new StringBuilder();
        for (String string : ses) {
            sb.append(string).append("\n");
        }
        JOptionPane.showMessageDialog(null, sb.toString());
        new Thread(new Runnable() {
            @Override
            public void run() {
                loadContent();
                try {
                    Thread.currentThread().join();
                } catch (InterruptedException ex) {}
            }
        }).start();
    }//GEN-LAST:event_txtHyperlinkUpdate
