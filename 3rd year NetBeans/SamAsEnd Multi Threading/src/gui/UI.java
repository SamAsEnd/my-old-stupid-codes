    private void fab(final int i, final JLabel ans) {
        ans.setText("Calculating . . . ");
        Thread thread;
        thread = new Thread(new Runnable() {
            @Override
            public void run() {
                int r = calc(i);
                System.out.println("done");
                ans.setText("" + r);
                try {
                    Thread.currentThread().join();
                } catch (InterruptedException ex) { }
            }

            private int calc(int i) {
                if (i == 0) {
                    return 0;
                }
                else if (i == 1) {
                    return 1;
                }
                else {
                    return calc(i - 1) + calc(i - 2);
                }
            }
        });
        thread.start();
    }
