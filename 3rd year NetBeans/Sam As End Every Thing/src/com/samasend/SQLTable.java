    private TableModel tableView(ResultSet result) throws SQLException {
        ResultSetMetaData metaData = result.getMetaData();
        String[][] data = new String[result.getFetchSize()+5][metaData.getColumnCount()+5];
        String[] columnNames = new String[metaData.getColumnCount()];
        for (int i = 0; i < metaData.getColumnCount(); i++) {
            columnNames[i] = metaData.getColumnName(i+1);
        }
        int i = 0;
        String tmp = null;
        while (result.next()) {
            for (int j = 0; j < metaData.getColumnCount(); j++) {
                int type = metaData.getColumnType(j+1);
                if (type == Types.VARCHAR) {
                    tmp = result.getString(j+1);
                } else if (type == Types.INTEGER) {
                    tmp = result.getInt(j+1)+"";
                }
                data[i][j] = tmp;
            }
            i++;
        }
        return new DefaultTableModel(data, columnNames);
    }
